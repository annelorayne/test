class CarroNovo{
	private String modelo;
	private int ano;
	

	public String getModeloNovo(){
		return modelo+" Novo";
	}	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
}

